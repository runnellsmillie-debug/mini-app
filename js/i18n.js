window.t = function(key) {
    const lang = window.state?.lang || "uz";
    return window.I18N[lang]?.[key] ?? window.I18N.uz[key] ?? key;
};

window.applyLang = function() {
    const lang = window.state?.lang || "uz";
    document.documentElement.lang = lang === "uz" ? "uz" : lang;
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (key) el.textContent = window.t(key);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        el.placeholder = window.t(el.getAttribute("data-i18n-placeholder"));
    });
    document.querySelectorAll("[data-i18n-title]").forEach(el => {
        el.title = window.t(el.getAttribute("data-i18n-title"));
    });
    document.querySelectorAll("[data-i18n-aria]").forEach(el => {
        el.setAttribute("aria-label", window.t(el.getAttribute("data-i18n-aria")));
    });

    const navMap = { home: "nav_home", add: "nav_add", other: "nav_other", report: "nav_report" };
    Object.keys(navMap).forEach(id => {
        const nav = document.getElementById("nav-" + id);
        const span = nav?.querySelector("span");
        if (span) span.textContent = window.t(navMap[id]);
    });

    const modeExp = document.getElementById("mode-exp");
    const modeInc = document.getElementById("mode-inc");
    if (modeExp) modeExp.textContent = window.t("expense");
    if (modeInc) modeInc.textContent = window.t("income");

    const chatInp = document.getElementById("profile-chat-input");
    if (chatInp) chatInp.placeholder = window.t("chat_input_ph");

    window.actMainCat = null;
    window.actSubCat = null;
    if (window.buildDetailedCategories) window.buildDetailedCategories();
    if (window.renderAddKeyboard) window.renderAddKeyboard(true);
    if (window.renderProfileRoleOptions) window.renderProfileRoleOptions();

    if (window.renderHomeTab) window.renderHomeTab();
    if (window.syncSettingsUI) window.syncSettingsUI();
    if (window.renderSidebar) window.renderSidebar();
    if (window.renderAddCats) window.renderAddCats();
    if (window.renderServicesMenu) window.renderServicesMenu();
    if (window.syncDescDisplay && !window.descStr) window.syncDescDisplay();
    if (window.render) window.render();
    if (window.renderReport && window.curTab === "report") window.renderReport();
    if (window.refreshBankSubViewTitle && window.curBankSub) window.refreshBankSubViewTitle();
    const confirmMsg = document.getElementById("confirm-msg");
    if (confirmMsg) confirmMsg.textContent = window.t("confirm_type_close");
    const confirmCode = document.getElementById("confirm-code");
    if (confirmCode) confirmCode.placeholder = window.t("confirm_placeholder");
    const kbDock = document.querySelector(".add-numpad-dock");
    if (kbDock) kbDock.setAttribute("aria-label", window.t("keyboard"));
    if (window.syncPlanUi) window.syncPlanUi();
    if (window.renderPlanned && window.curBankSub === "plan") window.renderPlanned();
    const fsModal = document.getElementById("modal-profile-fs");
    if (fsModal?.style.display === "flex" && window.renderProfilePermsGrid) {
        const perms = Array.from(document.querySelectorAll(".fs-perm-chk:checked")).map(c => c.value);
        window.renderProfilePermsGrid(perms);
        const profId = document.getElementById("fs-prof-id")?.value;
        if (window.renderWalletManageGrid) {
            const wm = window.getWalletManageChecked ? window.getWalletManageChecked() : [];
            window.renderWalletManageGrid(profId || null, wm);
        }
    }
};
